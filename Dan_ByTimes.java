public class Dan_ByTimes {

	public static void main(String[] args) {
		// 구구단 
		
		for ( int i = 2; i <10; i++) {
			
			for(int j = 1; j <10; j++) {
				System.out.println(i + " x " + j + " = " + (i * j));
			}
			
			System.out.println();		//이거 해도 단끼리 엔터 안쳐지는데??
		}
		
		

	}

}

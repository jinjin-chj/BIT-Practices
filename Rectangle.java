
public class Rectangle {

	int height;
	int width;
	
	public int getHeight() {		//getter.setterÇÔ¼ö
		return height;
	}
	
	public void setHeight(int height) {
		this.height = height;
	}
	
	public int getWidth() {
		return width;
	}
	
	public void setWidth(int width) {
		this.width = width;
	}
	
	public int getArea() {
		return width * height;
	}
}

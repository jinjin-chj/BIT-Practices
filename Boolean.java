
public class Boolean {

	public static void main(String[] args) {
		
		boolean b1 = true;
		boolean b2 = false;
		System.out.println(b1);		// b1이 지난 값 출력
		System.out.println(b2);
		
		int num1 = 10;
		int num2 = 20;
		System.out.println(num1 < num2);		//cpu가 처리해서 알려줌
		System.out.println(num1 > num2);
	}

}


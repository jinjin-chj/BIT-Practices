
public class ArithOp {	// Arithmetic Operation: 산술 연산자

	public static void main(String[] args) {
		int num1 = 7;
		int num2 = 3;
		
		System.out.println("num1 + num2 = " + (num1 + num2));
		System.out.println("num1 - num2 = " + (num1 - num2));
		System.out.println("num1 * num2 = " + (num1 * num2));
		System.out.println("num1 / num2 = " + ((double)num1 / num2));
		System.out.println("num1 % num2 = " + (num1 % num2));
			
	}

}
